                        <b>I am a man or use male pronouns, can I still join WiC?</b>
                        <ul>
                            <li> Of course, but some limits apply such as you will not be able to volunteer overnight at
                                After Hours. During the afternoon you are welcomed though. There are also some women only events
                                you will be unable to participate in such as WiCHacks (you can volunteer though) or applying
                                for scholarships like Grace Hopper.</li>
                        </ul>
                        <b>I am non-binary or do not use female pronouns, can I still join WiC?</b>
                        <ul>
                            <li>Of course, but applications for scholarships like Grace Hopper will depend on their organizations
                                guidelines.</li>
                        </ul>
                        <b>What is a WiC Ally?</b>
                        <ul>
                            <li>An RIT community member committed to furthering the mission and goals of Women in Computing, for the purpose of affecting positive change and fostering a more inclusive computing community.</li>
