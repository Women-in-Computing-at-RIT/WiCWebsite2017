                        <b>What is Women in Computing at RIT?</b>
                        <ul>
                            <li>WiC is an evolving professional organization within the RIT community. WiC is an inclusive organization
                                which supports all interested in computing while promoting the success and advancement of women in the
                                field. WiC inspires confidence in our members by fostering personal growth and professional development</li>
                        </ul>
                        <b>How do I join WiC?</b>
                        <ul>
                            <li> Come to one of our General Meetings every Wednesday or stop by the WiC space (GOL-2600)</li>
                        </ul>
                        <b>How do I become a General Member?</b>
                        <ul>
                            <li>Attend at least 7 of the general meetings</li>
                            <li>Participate in 1 WiC event (Outreach, WiCHacks, WiConnects, etc.)</li>
                        </ul>
                        <div class="row img-row">
                            <section class="page col-sm-6"><img class="center-block" src="img/FAQ/gen1.jpg"></section>
