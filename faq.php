                        </div>
                        <b>How do I become an Active Member?</b>
                        <ul>
                            <li>Attend at least 7 of the general meetings</li>
                            <li>Participate in 1 WiC event (Outreach, WiCHacks, WiConnects, etc.)</li>
                            <li>Attend and participate in at least 7 of the same committee’s weekly meetings</li>
                        </ul>
                        <b>What are the benefits of Active Membership?</b>
                        <ul>
                            <li> Attend conferences, WiC Jacket, priority to networking with university recruiters and
                                alumni, Leadership Opportunities and present at panels.</li>
                        </ul>
                        <b>Where is the WiC Space and when is it open?</b>
                        <ul>
                            <li>The WiC space is located in GOL-2600, the second floor of Golisano. Just follow the signs
                                and come say hi! It’s open from 9am - 4pm and various times at night</li>
                        </ul>
