                        <h4>STEM:</h4>
                        <p>WiConnects 2018 was a smashing success! For those who don’t know what this event is, in early March the Public Relations committee hand-delivers and sends invitations out to RSVP for our networking and dinner event to give opportunities for interactions and mentorship within the members of our college that would not happen organically because of being in different departments. With about 50 participants, every table had a mix of majors to meet new people in Golisano. This event began with networking with the group at your table, continued with a 3-course dinner, and concluded with guest speakers Erika Mesh and Dr. Sarah Huibregtse who enlightened us on their individual journey to technology and some inspiration shared through their life stories. </p> 
                        <p>Erika is a software engineering professor here in GCCIS, and she gave many pieces of advice in her talk relating to her love outside of her job--her horses. With such pieces of advice as “ride the horse you have,” it was a very relevant and memorable theme. Many people seemed to be impacted by her talk, because she had a collage pictures to depict her life as she described the journey to where she is currently. The description of her path through education and life would seem “out of order” to most, but the advice that you shouldn’t care about where you are in life, but rather enjoy the ride of how you put together a lot of the pieces along the way and do it in your own time was a key takeaway. Also the call to do what you need to do at the point of your life that it is relevant is what is important for your own self-growth. She took the lead in appearing to be a great mentor and passionate about teaching others through her life.</p>
                        <p>Sarah’s talk revolved around her experience in two branches of the military, and being at the forefront of technological teaching. In between a menagerie of jobs listing back to ice cream shops, schools, and submarines, her story came to light. In her current employment at RIT in the C-CORE department, which deals with outreach to K-12, she provides programs to get information and activities to younger people, so computing is in their mind as something that they can do as their future career. She inspired us to set goals for ourselves and never settle for less. The road to where she is now, through the early 2000s grade school classrooms and the military contributing to her multitude of opportunities, was an impressive cluster of events. It was clear that her passion for helping others to gain access to resources provisional to their success was also shown in her talk. </p>
                        <p>The night ended with a brief mingling session, everyone becoming friends and exiting as equals. Thank you again to the speakers and to the PR committee for such an awesome event!</p>
                        
                        <p>After parents hugged their children goodbye, we broke out into ice breakers. Popular games were played, causing laughter to fill the room. Once everyone felt comfortable with each other, groups were assigned for scavenger hunts around campus. Volunteers gave a tour of campus as the prospective students solved their riddles.</p>
                        
