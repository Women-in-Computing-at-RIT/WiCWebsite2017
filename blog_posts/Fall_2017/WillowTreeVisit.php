                    <h2 class="page-title">WillowTree Apps Visit</h2>
                    <div class="entry">
                        <h4>About WillowTree Apps:</h4>
                        <p>WillowTree Apps focuses on creating and maintaining digital products such as mobile apps and websites for their clients. Regal Entertainment Group, General Electrics Predix, and Canadian Broadcasting Company are just a few of their clients. Their headquarters is located in Charlottesville, SC, with a secondary office in Durham, SC. Their main focus includes but is not limited to: speedy media delivery, enterprise, healthcare, IoT devices, retail and hospitality. All teams within WillowTree are co-located to make the process more efficient.</p><br>
                        
                        <h4>Working With WiC:</h4>
                        <p>During their visit at RIT for the Career Fair, WillowTree Apps came to WiC to discuss who they are as a company and possible internship opportunities. The main topic was on their process for creating a digital product from start to finish. To end their visit they talked about working on diversifying their work atmosphere by including more women in STEM related fields.</p><br>
                        
                        <p><b>Q: </b> What is the product development cycle like at WillowTree Apps?
                        <br><b>A: </b>After a client approaches the company asking for an app, WillowTree Apps makes sure to understand the users by performing focus groups and user interviews. Afterwards, mock-ups are made, the product is designed, and content is decided. The next step is a critique phase in which we focus on the motion design and interaction. We create an interactive prototype and then the cycle starts again. In addition, the development process also includes analytics, API design, and review based on special cases with designers.</p>
                        
                        <p><b>Q: </b>What’s your favorite part of the app development cycle?
                        <br><b>A: </b>Animation is my favorite part of the development cycle. Animation includes manually drawing design ideas and putting them into use. The collaboration between all the teams where everyone respects other teams is key to the success during this part. It’s important to try to understand each other. Overall, it’s very fulfilling to see your design in action in a focus group and be involved in the work you’re putting out.</p>
                        
                        <p><b>Q: </b>What tools do you use at WillowTree Apps?
                        <br><b>A: </b>The tools we use are Sketch and Principle, Illustrator, Envision, and multiple other tools that help us facilitate the development process. We also use Zepplin for translating design to real things and Jira to move things to the QA team.</p>
                        
                        <p><b>Q: </b>Does the work at WillowTree Apps focus more on creating apps from scratch more or taking apps from other companies?
                        <br><b>A: </b>We tend to make more apps from scratch.For those that we do take over we want to make them our own. We customize it for the customer where we own what we do and help the client asking for the app. We do more than just listen.</p><br>
                        
                        <h4>Opportunities:</h4>
                        <p>WillowTree Apps offers full time and summer internships for iOS, Android, Web engineering, Product Designing, security, and QA. The company is not looking for a specific year or major, but rather, they desire experience. WillowTree Apps seeks students that have made a website, personal project app, online portfolios, or anything that displays the process of a designer.</p>
                        <p>At WillowTree Apps, interns will be working on a real project for a client. The company tries to match each intern with the best team that meets the student’s goals as a developer. WillowTree Apps provides an one-on-one mentor the duration of the internship. Employees also have the freedom to try other areas in the company.</p>
                        <p>In addition, WillowTree Apps provides relocation assistance for full time hires and a housing stipend for interns. Most interns are placed in Charlottesville at the main office.</p><br>
                        
